a = 10
b = 5

# Arithmetic Operations 

c = a + b      # Addition
d = a - b      # Subtraction 
e = a * b      # Multiplication → axb
f = a / b      # Division → divides a by b (float result)
g = a % b      # Modulus → gives remainder of a divided by b
k = a // b     # Floor Division → gives integer result of division
l = a ** b     # Exponentiation → raises (a^b)

# Bitwise Operations 

h = a ^ b      # Bitwise XOR 
i = a & b      # Bitwise AND
j = a | b      # Bitwise OR
m = a << 1     # Left Shift → shifts bits left (multiplies by 2)
n = a >> 1     # Right Shift → shifts bits right (divides by 2)

# Comparison Operations

o = a == b     # Equal to → checks if a and b are equal
p = a != b     # Not equal to → checks if a and b are different
q = a > b      # Greater than → checks if a is greater than b
r = a < b      # Less than → checks if a is less than b
s = a >= b     # Greater than or equal to
t = a <= b     # Less than or equal to

# Print Results

print(c, d, e, f, g, k, l)
print()
print(h, i, j, m, n)
print()
print(o, p, q, r, s, t)
